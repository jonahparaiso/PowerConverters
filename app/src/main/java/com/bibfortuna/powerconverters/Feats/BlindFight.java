package com.bibfortuna.powerconverters.Feats;

public class BlindFight extends Feat {
    public BlindFight() {
        super("BLIND FIGHT", "You know how to fight in melee without being able to see your foes.\n" +
                "   Benefit: In melee combat, every time you miss because of concealment, you can reroll\n" +
                "       your miss chance percentile roll one time to see if you actually hit.\n" +
                "       You take only half the usual penalty to speed for being unable to see. Darkness and\n" +
                "       poor visibility in general reduces your speed to three-quarters of normal, instead of one-half.");
    }
}
